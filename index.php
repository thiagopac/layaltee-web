                            </h4>
                            <div id="collapse1" class="panel-collapse collapse in">
                                <p>Após pagar a conta, dentro do painel de gerencialmente do Loyaltee, você apresentará um QR-Code para seu cliente, ele fará a leitura deste QR-Code dentro do aplicativo do seu estabelecimento e o cupom será gerado automaticamente. Este QR-Code é válido somente para aquele dia, não podendo ser lido pelo mesmo cliente mais de uma vez no dia, no entanto, você pode adicionar o cupom para seu cliente manualmente, caso alguma limitação seja apresentada no momento, como um aparelho celular com problema na câmera, ou aquela é a segunda conta que o cliente está pagando naquele dia. Esta limitação evita fraudes e todo cupom gerado tem seu registro de horário, dia e intermediador. Também é possível verificar a quantidade de cupons do cliente, validando se ele atingiu a quantidade necessária para receber a premiação que você definiu.</p>
