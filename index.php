function toggleAlert(){
    $(".alert").toggleClass('in out');
    return false; // Keep close.bs.alert event from removing from DOM
}

$("#formContact").submit(function(e) {
    e.preventDefault();

    var valName = $("#name").val();
    var valLocal = $("#local").val();
    var valEmail = $("#email").val();
    var valTelephone = $("#telephone").val();
    var valMessage = $("#message").val();

    $.ajax({
        url: 'https://www.loyaltee.com.br/manager/webapi/site/contact',
        type: 'POST',
        contentType: 'application/json',
        dataType:"json",
        async: true,
        data: JSON.stringify({name: valName, local: valLocal, email: valEmail, telephone: valTelephone, message: valMessage}),
        success: function (result) {
          var response = result;

          // console.log(response);

          if(response["status"] == 1){

            toggleAlert();
            $('#bsalert').on('close.bs.alert', toggleAlert)

            $("#formContact").trigger("reset");

          }


        }, error: function (result) {

        }
    });

});
